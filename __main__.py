from go_to_api_n2yo import (
    go_to_url,
    get_tle,
    get_satelit_position,
    get_visual_passes,
    get_radio_passes,
    get_above,
)


def main():
    go_to_url()
    satelite_tle = get_tle()
    print("\n")
    print("Function Tle")
    print("Satelite ID:", satelite_tle.sat_id)
    print("Satelite Name:", satelite_tle.sat_name)
    print("Satelite Transactions Count:", satelite_tle.transactions_count)
    print("\n")
    satelite_position, positions = get_satelit_position()
    print("Function Satelite info")
    print("Satelite ID:", satelite_position.sat_id)
    print("Satelite Name:", satelite_position.sat_name)
    print("Satelite Transactions Count:", satelite_position.transactions_count)
    print("Satelito Positions")
    print("satlatitude:", positions.sat_latitude)
    print("satlongitude:", positions.sat_longitude)
    print("sataltitude:", positions.sat_altitude)
    print("azimuth:", positions.azimuth)
    print("elevation:", positions.elevation)
    print("ra:", positions.ra)
    print("dec:", positions.dec)
    print("timestamp:", positions.timestamp)
    print("eclipsed", positions.eclipsed)
    satelite_visual, passes = get_visual_passes()
    print("\n")
    print("Function Get visual Passes")
    print("info")
    print("Satelite ID:", satelite_visual.sat_id)
    print("Satelite Name:", satelite_visual.sat_name)
    print("Satelite Transactions Count:", satelite_visual.transactions_count)
    print("Satelite Passes Count:", satelite_visual.passes_count)
    print("Visual Passes")
    print("startAz:", passes.start_az)
    print("startAzCompass:", passes.start_az_compass)
    print("startEl:", passes.start_el)
    print("startUTC:", passes.start_UTC)
    print("maxAz:", passes.max_az)
    print("maxAzCompass:", passes.max_az_compass)
    print("maxEl:", passes.max_el)
    print("maxUTC:", passes.max_UTC)
    print("endAz:", passes.end_az)
    print("endAzCompass:", passes.end_az_compass)
    print("endEl:", passes.end_el)
    print("endUTC:", passes.end_UTC)
    print("mag:", passes.mag)
    print("duration:", passes.duration)
    print("startVisibility:", passes.start_visibility)
    print("\n")
    satelite_radio, radio_passes = get_radio_passes()
    print("Function Radio Passes")
    print("info")
    print("Satelite ID:", satelite_radio.sat_id)
    print("Satelite Name:", satelite_radio.sat_name)
    print("Satelite Transactions Count:", satelite_radio.transactions_count)
    print("Satelite Passes Count:", satelite_radio.passes_count)
    print("passes")
    print("startAz:", radio_passes.start_az)
    print("startAzCompass:", radio_passes.start_az_compass)
    print("startEl:", radio_passes.start_el)
    print("startUTC:", radio_passes.start_UTC)
    print("maxAz:", radio_passes.max_az)
    print("maxAzCompass:", radio_passes.max_az_compass)
    print("maxEl:", radio_passes.max_el)
    print("maxUTC:", radio_passes.max_UTC)
    print("endAz:", radio_passes.end_az)
    print("endAzCompass:", radio_passes.end_az_compass)
    print("endEl:", radio_passes.end_el)
    print("endUTC:", radio_passes.end_UTC)
    print("mag:", radio_passes.mag)
    print("duration:", radio_passes.duration)
    print("startVisibility:", radio_passes.start_visibility)
    satelite_above, above = get_above()
    print("\n")
    print("Function Above")
    print("info")
    print("Satelite ID:", satelite_above.sat_id)
    print("Satelite Name:", satelite_above.sat_name)
    print("Satelite Transactions Count:", satelite_above.transactions_count)
    print("Satelite Passes Count:", satelite_above.passes_count)
    print("Satelite Category:", satelite_above.category)
    print("Satelite Sat Count:", satelite_above.sat_count)
    print("Above")
    print("Sat Id", above.sat_id)
    print("Sat Name", above.sat_name)
    print("Int designator", above.int_designator)
    print("Launch date", above.launch_date)
    print("Sat Lat", above.sat_lat)
    print("Sat Lng", above.sat_lng)
    print("Sat alt", above.sat_alt)


if __name__ == "__main__":
    main()

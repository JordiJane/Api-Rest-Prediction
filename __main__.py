from go_to_api_n2yo import (
    go_to_url,
    get_tle,
    get_satelit_position,
    get_visual_passes,
    get_radio_passes,
)


def main():
    go_to_url()
    satelite = get_tle()
    print("\n")
    print("Tle")
    print("Satelite ID:", satelite.sat_id)
    print("Satelite Name:", satelite.sat_name)
    print("Satelite Transactions Count:", satelite.transactions_count)
    print("\n")
    satelite2, positions = get_satelit_position()
    print("Satelite info")
    print("Satelite ID:", satelite2.sat_id)
    print("Satelite Name:", satelite2.sat_name)
    print("Satelite Transactions Count:", satelite2.transactions_count)
    print("Satelito Positions")
    print("satlatitude:", positions.satlatitude)
    print("satlongitude:", positions.satlongitude)
    print("sataltitude:", positions.sataltitude)
    print("azimuth:", positions.azimuth)
    print("elevation:", positions.elevation)
    print("ra:", positions.ra)
    print("dec:", positions.dec)
    print("timestamp:", positions.timestamp)
    print("eclipsed", positions.eclipsed)
    satelite3, passes = get_visual_passes()
    print("\n")
    print("Get visual Passes")
    print("info")
    print("Satelite ID:", satelite3.sat_id)
    print("Satelite Name:", satelite3.sat_name)
    print("Satelite Transactions Count:", satelite3.transactions_count)
    print("Satelite Passes Count:", satelite3.passes_count)
    print("Visual Passes")
    print("startAz:", passes.startAz)
    print("startAzCompass:", passes.startAzCompass)
    print("startEl:", passes.startEl)
    print("startUTC:", passes.startUTC)
    print("maxAz:", passes.maxAz)
    print("maxAzCompass:", passes.maxAzCompass)
    print("maxEl:", passes.maxEl)
    print("maxUTC:", passes.maxUTC)
    print("endAz:", passes.endAz)
    print("endAzCompass:", passes.endAzCompass)
    print("endEl:", passes.endEl)
    print("endUTC:", passes.endUTC)
    print("mag:", passes.mag)
    print("duration:", passes.duration)
    print("startVisibility:", passes.startVisibility)
    print("\n")
    satelite4, passes2 = get_radio_passes()
    print("Radio Passes")
    print("info")
    print("Satelite ID:", satelite4.sat_id)
    print("Satelite Name:", satelite4.sat_name)
    print("Satelite Transactions Count:", satelite4.transactions_count)
    print("Satelite Passes Count:", satelite4.passes_count)
    print("passes")
    print("startAz:", passes2.startAz)
    print("startAzCompass:", passes2.startAzCompass)
    print("startEl:", passes2.startEl)
    print("startUTC:", passes2.startUTC)
    print("maxAz:", passes2.maxAz)
    print("maxAzCompass:", passes2.maxAzCompass)
    print("maxEl:", passes2.maxEl)
    print("maxUTC:", passes2.maxUTC)
    print("endAz:", passes2.endAz)
    print("endAzCompass:", passes2.endAzCompass)
    print("endEl:", passes2.endEl)
    print("endUTC:", passes2.endUTC)
    print("mag:", passes2.mag)
    print("duration:", passes2.duration)
    print("startVisibility:", passes2.startVisibility)


if __name__ == "__main__":
    main()
